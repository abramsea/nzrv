$(window).scroll(function(){
    if($(window).scrollTop()>220){
        $('.header__nav-link').removeClass('header__nav-link_theme_dark');
        $('.header__communication-item').removeClass('header__communication-item_theme_dark');
        $('.header__icon').removeClass('header__icon_theme_dark');
        $('.header__nav-link').addClass('header__nav-link_theme_light');
        $('.header__communication-item').addClass('header__communication-item_theme_light');
        $('.header__icon').addClass('header__icon_theme_light');
        $('.header-container').addClass('header-container_fixed')
    }
})

